package Oppgave1;

public class A {
    public static void main(String[] args) {
        int a = 7;
        int b = a / 3; // 2
        int c = a / 4 * 4; // 1 * 4 = 4
        System.out.println(a); // 7
        System.out.println(b); // 2
        System.out.println(c); // 4
        System.out.printf("a=%d, b=%d, c=%d%n", a, b, c); // a=7, b=2, c=4
    }
}
